int	ft_recursive_power(int nb, int power)
{
	int	x;

	x = nb;
	if (power < 0)
		return (0);
	if (power == 0)
		return (1);
	else
	{
		nb = (x * ft_recursive_power(nb, (power - 1)));
	}
	return (nb);
}
